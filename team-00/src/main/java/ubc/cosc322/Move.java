package ubc.cosc322;

//Class for movement description
public class Move {
	int QX;
	int QY;
	int QmoveX;
	int QmoveY;
	int arrowX;
	int arrowY;
	
	public Move(int QX,int QY,int QmoveX, int QmoveY,int arrowX,int arrowY) {
		this.QX=QX;
		this.QY=QY;
		this.QmoveX =QmoveX;
		this.QmoveY = QmoveY;
		this.arrowX = arrowX;
		this.arrowY = arrowY;
		
		
	}
	
	


}
